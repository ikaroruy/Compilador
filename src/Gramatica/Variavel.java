package Gramatica;

public class Variavel {
	private String nome;
	private String tipo;
	private String conteudo;
	private int indiceNaLinha;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getConteudo() {
		return conteudo;
	}
	public void setConteudo(String conteudo) {
		this.conteudo = conteudo;
	}
	public int getIndiceNaLinha() {
		return indiceNaLinha;
	}
	public void setIndiceNaLinha(int indiceNaLinha) {
		this.indiceNaLinha = indiceNaLinha;
	}
}
